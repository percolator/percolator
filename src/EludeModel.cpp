double RTModel::getNoPtms(string pep) {
  double ptms = 0;
  string::size_type posP = 0;
  while (posP < ptmAlphabet.length()) {
    string::size_type pos = 0;
    while ((pos = pep.find(ptmAlphabet[posP], pos)) != string::npos) {
      pep.replace(pos, 1, "");
      ++pos;
      ++ptms;
    }
    ++posP;
  }
  return ptms;
}

